#Escreva um programa que solicite ao usuário a idade do seu animal de estimação e a converta em anos humanos. 
# Imprima o resultado. Lembre-se de que 1 ano canino equivale a 7 anos humanos.
idadeAnimal = int(input("Insira a idade do seu animal de estimação: "))
idadeHumana = idadeAnimal * 7
print(f"A idade do seu animal de estimação equivale a {idadeHumana} anos humanos.")